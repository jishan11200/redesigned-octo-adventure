            <h3>M - N</h3>
            <ul>
              <li><i class="bi bi-check2-circle"></i><strong>Manifest:</strong> A document listing the cargo on board a vessel, flight, or other mode of transportation. It includes details such as quantity, weight, and description of each shipment.</li>
              <li><i class="bi bi-check2-circle"></i><strong>NVOCC (Non-Vessel Operating Common Carrier):</strong>  A company that consolidates and coordinates shipments from multiple shippers to achieve cost savings. NVOCCs do not own vessels but act as carriers.</li>
            </ul>
            <h3>O - P</h3>
            <ul>
              <li><i class="bi bi-check2-circle"></i><strong>Origin:</strong> The point where the journey of goods begins, usually the place of manufacture or shipment.</li>
              <li><i class="bi bi-check2-circle"></i><strong>Port of Discharge:</strong> The port where a vessel unloads its cargo, and the goods are discharged.</li>
            </ul>
