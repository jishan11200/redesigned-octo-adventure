              <h3>E - F</h3>
              <ul>
                <li><i class="bi bi-check2-circle"></i><strong>Freight Forwarder:</strong> An intermediary or agent that organizes the transportation of goods on behalf of a shipper. Freight forwarders handle logistics, documentation, and customs clearance.</li>
                <li><i class="bi bi-check2-circle"></i><strong>Incoterms (International Commercial Terms):</strong> A set of standardized trade terms published by the International Chamber of Commerce (ICC) that define the responsibilities of sellers and buyers in international trade transactions.</li>
              </ul>
              <h3>G - H</h3>
              <ul>
                <li><i class="bi bi-check2-circle"></i><strong>Goods in Transit:</strong> Goods being transported from the seller to the buyer but not yet received at the destination.</li>
                <li><i class="bi bi-check2-circle"></i><strong>High Cube Container:</strong> A container with extra height, typically 9.6 feet, allowing for the transportation of taller cargo.</li>
              </ul>
            </div>
          </div>
  
