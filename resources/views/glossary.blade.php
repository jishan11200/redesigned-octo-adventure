              <h3>A - B</h3>
              <ul>
                <li><i class="bi bi-check2-circle"></i><strong>Bill of Lading (B/L):</strong> A legal document issued by a carrier to the shipper, acknowledging the receipt of goods for shipment and specifying the terms of transportation.</li>
                <li><i class="bi bi-check2-circle"></i><strong>Booking: </strong> The process of reserving space on a vessel or other mode of transportation for the carriage of goods.</li>
              </ul>
              <h3>C - D</h3>
              <ul>
                <li><i class="bi bi-check2-circle"></i><strong>Container:</strong>  A standardized, reusable metal box used for the transportation of goods. Containers come in various sizes and types, including standard dry containers, refrigerated containers (reefers), and specialized containers for specific cargo types.</li>
                <li><i class="bi bi-check2-circle"></i><strong>Customs Clearance:</strong> The process of complying with customs regulations to allow the import or export of goods, including the submission of required documentation and payment of duties and taxes.</li>
              </ul>
              
