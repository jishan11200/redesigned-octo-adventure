            <h3>I - J</h3>
              <ul>
                <li><i class="bi bi-check2-circle"></i><strong>Inland Transportation:</strong> The movement of goods within a country, typically by road, rail, or inland waterways, connecting ports to final destinations.</li>
                <li><i class="bi bi-check2-circle"></i><strong>Joint Venture:</strong> A business arrangement where two or more parties collaborate to undertake a specific project or business activity.</li>
              </ul>
            <h3>K - L</h3>
            <ul>
              <li><i class="bi bi-check2-circle"></i><strong>LCL (Less than Container Load):</strong> A shipping arrangement for goods that do not fill a standard container. Multiple shipments from different shippers are consolidated into a single container.</li>
              <li><i class="bi bi-check2-circle"></i><strong>Letter of Credit (L/C):</strong> A financial document issued by a bank on behalf of a buyer, providing payment to a seller once specified conditions are met.</li>
            </ul>
          </div>
