          <h3>U - V</h3>
          <ul>
            <li><i class="bi bi-check2-circle"></i><strong>Ullage:</strong> The empty space between the top of a liquid cargo and the top of its container, often expressed as a percentage.</li>
            <li><i class="bi bi-check2-circle"></i><strong>Vessel:</strong>  A ship or large boat used for transporting goods across seas and oceans.</li>
          </ul>
          <h3>W - X - Y - Z</h3>
          <ul>
            <li><i class="bi bi-check2-circle"></i><strong>Warehouse:</strong>A facility used for the storage of goods before, during, or after transportation.</li>
            <li><i class="bi bi-check2-circle"></i><strong>X-Ray Inspection:</strong> The use of X-ray technology to inspect the contents of containers and packages for security and customs purposes.</li>
            <li><i class="bi bi-check2-circle"></i><strong>Yard: </strong> An area used for the storage, staging, and handling of containers and cargo within a port or transportation terminal.</li>
            <li><i class="bi bi-check2-circle"></i><strong>Zone D (Dangerous Zone):</strong> A designated area within a vessel where dangerous goods are stowed.</li>
          </ul>
