          <h3>Q - R</h3>
            <ul>
              <li><i class="bi bi-check2-circle"></i><strong>Quarantine:</strong> A period during which goods are isolated to prevent the spread of disease or contamination.</li>
              <li><i class="bi bi-check2-circle"></i><strong>Reefer:</strong> A refrigerated container used for transporting temperature-sensitive cargo, such as perishable goods.</li>
            </ul>
          <h3>S - T</h3>
          <ul>
            <li><i class="bi bi-check2-circle"></i><strong>Shipment Tracking:</strong> The process of monitoring and recording the movement of goods in transit, providing real-time updates on their status and location.</li>
            <li><i class="bi bi-check2-circle"></i><strong>TEU (Twenty-foot Equivalent Unit): </strong> A standard unit of measurement in the shipping industry, representing the cargo-carrying capacity of a standard twenty-foot container.</li>
          </ul>
